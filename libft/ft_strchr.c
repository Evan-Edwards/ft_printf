#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (NULL);
}
/*The strchr() function searches for one character in a string. If it finds the
 character, it returns a pointer to the first occurence of this specific 
 character.*/